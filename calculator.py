#program to perform all the operations on two numbers 
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
res = num1 + num2 
print("Sum is",res)
res = num1 - num2 
print("Difference is",res)
res = num1 * num2 
print("Product is",res)
res = num1 / num2 
print("Division is",res)
res = num1 // num2 
print("Quotient is",res)
res = num1 % num2 
print("Remainder is",res)